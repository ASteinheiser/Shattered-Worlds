public class Player{

	private int health, playerSpeed, level, exp, gold;

	private int getHealth(){
		return health;
	}
	public int getPlayerSpeed(){
		return playerSpeed;
	}
	private int getLevel(){
		return level;
	}
	private int getExp(){
		return exp;
	}
	private int getGold(){
		return gold;
	}
}